		<h3>Bio</h3>
		<p class="bio">
		Nick Johnston is a Canadian instrumental guitarist. Still relatively young (under 30) and unknown he is quickly gaining a virtuoso reputation
		in the guitar players circles. He got interested in the guitar at the age of 12 after hearing his neighbour play and he hasnt stopped practicing
		ever since. Nick Johnstons influences include the likes of Yngwee Malmsten and Eddie Van Halen but his music is quite different from both as
		Nick has a very unique playing style utilizing a lot of fast sweep-picking and fast slides.
		<br><br>He is a self taught artist who has released 3 solo albums which all include very different styles of music as his playing takes him from
		rock to blues to gypsy and desert music.
		<br><br>His last album was a huge success and he has become the first Custom Shop artist of the guitar manufacturing company Shecter.
		He is also a Seymour Duncan supported artist.
		</p>
		<p class="comment">
		Why he made the list:
		<br><br>
		I discovered Nick from seeing a YouTube video of an interview he did for Anderton's guitar store just a few months ago.
		He has a great vintage sounding tone and his play is second to none. Like Satriani though it's not his
		fast play that makes him great to listen to but the mood and melody he creates with this playing. He is an exceptional
		player who will definitely garner fame in the years to come.
		<br><br>
		- Rain
